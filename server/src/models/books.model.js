import mongoose from "mongoose";

const bookSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true,
    },
    author: {
      type: String,
      required: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    status: {
      type: String,
      enum: ["AVAILABLE", "BORROWED"],
      default: "AVAILABLE",
    },
  },
  { timestamps: true }
);

const books = mongoose.model("Book", bookSchema);

export { books };
